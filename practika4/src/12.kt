fun main() {
    // Создаем мапу для хранения соответствия страны и национальности
    val nationalities = mapOf(
        "США" to "Американец",
        "Россия" to "Русский",
        "Япония" to "Японец",
        "Франция" to "Француз",
        "Китай" to "Китаец",
        "Германия" to "Немец",
        "Италия" to "Итальянец",
        "Бразилия" to "Бразилец",
        "Великобритания" to "Англичанин",
        "Индия" to "Индиец",
        "Канада" to "Канадец",
        "Австралия" to "Австралиец"
    )

    // Запрашиваем у пользователя страну
    print("Введите страну: ")
    val country = readLine()!!.trim()

    // Проверяем, есть ли страна в мапе
    if (country in nationalities) {
        // Выводим национальность, соответствующую стране
        println("Национальность: ${nationalities[country]}")
    } else {
        // Выводим сообщение об отсутствии страны в мапе
        println("Страна не найдена в базе данных.")
    }
}