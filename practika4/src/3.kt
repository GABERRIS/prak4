fun main() {
    // Ввод оценки в виде числа от 0 до 100
    print("Введите оценку (0-100): ")
    val оценка = readLine()?.toIntOrNull()

    // Проверка на корректный ввод
    if (оценка != null && оценка in 0..100) {
        // Вывод оценки в виде буквенного значения
        when (оценка) {
            in 90..100 -> println("Отлично")
            in 80..89 -> println("Хорошо")
            in 70..79 -> println("Удовлетворительно")
            else -> println("Неудовлетворительно")
        }
    } else {
        println("Некорректный ввод.")
    }
}